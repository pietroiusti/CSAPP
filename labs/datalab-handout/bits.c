/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
    /*
      We can exploit the fact that that the sign bit changes if we
      add/subtract enough. Going from a negative number a non-negative
      number entails that the most significant bit goes from 1 to
      0. Going from a non-negative number to a negative number entails
      that the most signficant bit goes from 0 to 1.

      So, for example, if you want to check whether x is greater or
      equal than y, you can check whether -y + x yields a bit vector
      whose most significant bit is 0.

      And if you want to check whether x is less than or equal to y,
      you can check whether y - x yields a a bit vector whose most
      significant bit is 0.
     */

    int mask = 0x1<<31;

    return !( (((~0x30)+1) + x) & mask) // x is greater than or equal to 0x30
           &
           !( (0x39 + ((~x)+1)) & mask ); //x is less than or equal to 0x39
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  /*
    Chatgpt got it right at the first try. Here is their explanation:

    Compute x_nonzero: Using !!x, we convert any non-zero x to 1 and
    zero to 0.

    Create mask: We compute mask = ~x_nonzero + 1, which results in
    0xFFFFFFFF (all ones) when x_nonzero is 1, and 0x00000000 (all
    zeros) when x_nonzero is 0.

    Select y or z: Finally, we use bitwise operations to select y or
    z:

    When mask is all ones, (mask & y) yields y and (~mask & z) yields
    0.

    When mask is zero, (mask & y) yields 0 and (~mask & z) yields z.

    Combining these with bitwise OR gives you the desired result.
     */
    int x_nonzero = !!x;                // x_nonzero is 1 if x != 0, else 0
    int mask = ~x_nonzero + 1;          // mask is all ones if x != 0, else 0
    return (mask & y) | (~mask & z);
}
